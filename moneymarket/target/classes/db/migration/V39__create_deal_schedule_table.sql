-- V39: Create DEAL_SCHEDULE table for Deal Booking & Scheduler functionality
CREATE TABLE DEAL_SCHEDULE (
    SCHEDULE_ID         BIGINT AUTO_INCREMENT PRIMARY KEY,
    ACCOUNT_NUMBER      VARCHAR(30)     NOT NULL COMMENT 'Deal account number',
    OPERATIVE_ACCOUNT_NO VARCHAR(30)    NULL     COMMENT 'Customer operative account for MAT_PAY processing',
    CUSTOMER_ID         VARCHAR(20)     NULL,
    DEAL_TYPE           CHAR(1)         NOT NULL COMMENT 'L=Liability, A=Asset',
    EVENT_CODE          VARCHAR(20)     NOT NULL COMMENT 'INT_PAY, MAT_PAY',
    SCHEDULE_DATE       DATE            NOT NULL,
    SCHEDULE_AMOUNT     DECIMAL(18,2)   NULL,
    CURRENCY_CODE       CHAR(3)         NOT NULL,
    STATUS              VARCHAR(15)     NOT NULL DEFAULT 'PENDING' COMMENT 'PENDING, EXECUTED, FAILED, CANCELLED',
    EXECUTION_DATE_TIME TIMESTAMP       NULL,
    EXECUTED_BY         VARCHAR(20)     NULL,
    ERROR_CODE          VARCHAR(50)     NULL,
    ERROR_MESSAGE       VARCHAR(500)    NULL,
    CREATED_DATE_TIME   TIMESTAMP       NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CREATED_BY          VARCHAR(20)     NOT NULL,
    LAST_UPDATED_DATE_TIME TIMESTAMP    NULL ON UPDATE CURRENT_TIMESTAMP,
    LAST_UPDATED_BY     VARCHAR(20)     NULL,

    UNIQUE KEY unique_schedule (ACCOUNT_NUMBER, EVENT_CODE, SCHEDULE_DATE),
    INDEX idx_schedule_execution (SCHEDULE_DATE, STATUS),
    INDEX idx_account (ACCOUNT_NUMBER),
    INDEX idx_customer (CUSTOMER_ID)
);
